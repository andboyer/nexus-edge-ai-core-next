#[cfg(test)]
mod tests {
    use super::*;
    use argon2::{Algorithm, Params, Version};

    #[test]
    fn argon2_defaults_match_owasp_2024_baseline() {
        // The OWASP "Password Storage Cheat Sheet" baseline for
        // argon2id (2024): m_cost = 19_456 KiB (19 MiB),
        // t_cost = 2, p_cost = 1. If a future `argon2` crate
        // bump weakens these, this test fires so we can pin
        // explicit params instead of trusting Default.
        let p = Params::default();
        assert_eq!(
            p.m_cost(),
            19_456,
            "m_cost (KiB) drifted from OWASP-2024 baseline"
        );
        assert_eq!(p.t_cost(), 2, "t_cost drifted from OWASP-2024 baseline");
        assert_eq!(p.p_cost(), 1, "p_cost drifted from OWASP-2024 baseline");

        // Also pin the algorithm + version, in case Default ever
        // shifts away from argon2id v0x13. The `Argon2` struct
        // itself has no public algorithm getter, so we round-trip
        // through the PHC string to confirm the prefix.
        let a = Argon2::default();
        let salt = SaltString::generate(&mut OsRng);
        let phc = a
            .hash_password(b"long-enough-password", &salt)
            .unwrap()
            .to_string();
        assert!(phc.starts_with("$argon2id$v=19$"), "PHC prefix = {phc}");
        // And the `Algorithm` enum's `Argon2id` is the default
        // we rely on, not Argon2i/Argon2d.
        assert_eq!(Algorithm::default(), Algorithm::Argon2id);
        assert_eq!(Version::default(), Version::V0x13);
    }

    #[test]
    fn hash_and_verify_round_trip() {
        let plain = "correct-horse-battery-staple";
        let phc = hash_password(plain).expect("hash ok");
        assert!(phc.starts_with("$argon2id$"));
        assert!(verify_password(plain, &phc), "round-trip verify");
        assert!(
            !verify_password("wrong-horse-battery-staple", &phc),
            "wrong password fails"
        );
    }

    #[test]
    fn hash_produces_distinct_outputs_per_call() {
        // Salt is freshly random per call → two hashes of the
        // same password are different but both verify.
        let plain = "long-enough-distinct-test-pw";
        let a = hash_password(plain).unwrap();
        let b = hash_password(plain).unwrap();
        assert_ne!(a, b, "salts must differ");
        assert!(verify_password(plain, &a));
        assert!(verify_password(plain, &b));
    }

    #[test]
    fn verify_password_returns_false_for_malformed_hash() {
        // Returns false (not Err / panic) for garbage so the
        // caller cannot accidentally branch on "is the stored
        // hash even parseable" which would be a side-channel.
        assert!(!verify_password("anything", ""));
        assert!(!verify_password("anything", "not-a-phc-string"));
        assert!(!verify_password(
            "anything",
            "$bcrypt$v=2a$10$abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUV"
        ));
    }

    #[test]
    fn policy_rejects_short_passwords() {
        assert_eq!(
            check_password_policy("short").unwrap_err(),
            PasswordPolicyError::TooShort
        );
        // 11 chars — just under the limit.
        assert_eq!(
            check_password_policy("eleven-char").unwrap_err(),
            PasswordPolicyError::TooShort
        );
        // Exactly 12 — passes (assuming not in denylist).
        check_password_policy("zaphod-trout").expect("12 chars ok");
    }

    #[test]
    fn policy_counts_unicode_scalars_not_bytes() {
        // "über-secret" is 11 chars but >11 bytes (UTF-8). We
        // count chars, so it correctly rejects.
        assert_eq!(
            check_password_policy("über-secret").unwrap_err(),
            PasswordPolicyError::TooShort
        );
        // "über-secretz" is 12 chars → ok.
        check_password_policy("über-secretz").expect("12 unicode chars ok");
    }

    #[test]
    fn policy_rejects_common_passwords_case_insensitive() {
        // From the embedded denylist; these are all 12+ chars
        // so they get past the length check first.
        let common = [
            "password1234",
            "PASSWORD1234",
            "Password1234",
            "qwertyuiop12",
        ];
        for p in common {
            let err = check_password_policy(p).expect_err(p);
            assert_eq!(err, PasswordPolicyError::Common, "{p}");
        }
    }

    #[test]
    fn hash_password_runs_policy_first() {
        // Short → never reaches argon2.
        match hash_password("nope") {
            Err(PasswordError::Policy(PasswordPolicyError::TooShort)) => {}
            other => panic!("expected TooShort, got {other:?}"),
        }
        // Common → rejected with Common variant.
        match hash_password("password1234") {
            Err(PasswordError::Policy(PasswordPolicyError::Common)) => {}
            other => panic!("expected Common, got {other:?}"),
        }
    }

    #[test]
    fn denylist_loads_some_canonical_entries() {
        // Sanity-check that the embedded file actually parsed
        // and the worst-of-the-worst made it through the
        // include_str! → HashSet path. After the SecLists
        // top-1000 expansion the set is ~1260 entries; the
        // lower bound here is deliberately loose so that a
        // future prune (e.g. removing entries < MIN_PASSWORD_LEN
        // once we decide they have no audit value) won't trip
        // the test, but any catastrophic load failure still does.
        let set = common_password_set();
        assert!(
            set.len() > 1000,
            "denylist surprisingly small: {}",
            set.len()
        );
        for canonical in ["password1234", "qwertyuiop12", "letmein12345"] {
            assert!(set.contains(canonical), "denylist missing {canonical}");
        }
        // Spot-check that the SecLists top-1000 block landed.
        // These are short (< MIN_PASSWORD_LEN) and therefore
        // unreachable by real signups, but their presence
        // proves the embedded file includes the full corpus.
        for seclists in ["123456", "qwerty", "iloveyou", "monkey"] {
            assert!(
                set.contains(seclists),
                "SecLists top-1000 entry missing: {seclists}"
            );
        }
    }
}
